package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int m = 0;
        while (m < 5) {
            int n = 0;
            while (n < 10) {
                System.out.print("Q");
                n++;
            }
        System.out.println("");
        m++;
        }
    }
}